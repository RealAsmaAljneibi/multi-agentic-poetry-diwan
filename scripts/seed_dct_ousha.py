def main() -> None:
    records = []
    for url, md in OUSHA_POEMS:
        rec = parse_poem_md(md, url, "ousha_bint_khalifa")
        records.append(rec)
        print(f"  ✓ {rec['verse_id']:14s}  {rec['title'][:46]:46s}  {rec['bayt_count']} bayts  rhyme: {rec['meta']['rhyme']}")

    # Merge into existing dct_corpus.json (idempotent by verse_id)
    if OUT.exists():
        existing = json.loads(OUT.read_text(encoding="utf-8"))
    else:
        existing = {}
    bucket = existing.setdefault("ousha_bint_khalifa", [])
    seen = {r["verse_id"] for r in bucket}
    added = 0
    for rec in records:
        if rec["verse_id"] not in seen:
            bucket.append(rec)
            seen.add(rec["verse_id"])
            added += 1
    OUT.write_text(json.dumps(existing, ensure_ascii=False, indent=2), encoding="utf-8")
    total_bayts = sum(r["bayt_count"] for r in bucket)
    print(f"\n  → wrote {OUT}")
    print(f"  → Ousha now has {len(bucket)} poems / {total_bayts} bayts in the DCT corpus  (added {added} this run)")


if __name__ == "__main__":
    main()
